import numpy as np

intarr = np.array([[1, 2, 3], [4, 5, 6]])
print("Integer array:")
print(intarr)
print("")

floatarr = np.array([[1., 2, 3], [4, 5, 6]])
print("Float array:")
print(floatarr)
print("")

complexarr = np.array([[1, 2, 3], [4, 5, 6]], dtype=np.complex_)
print("Complex array:")
print(complexarr)
print("")
